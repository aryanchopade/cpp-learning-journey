#include <iostream>
using namespace std;


int main(){

    //bitwise AND (&)
    cout << "bitwise AND (&) operator : " << (4 & 8) << endl;

    //bitwise OR ( | )
    cout << "bitwise OR ( | ) operator : " << (4 | 8)<< endl;

    //bitwise XOR/exclusive-OR (^)
    cout << "bitwise XOR/exclusive-OR (^) operator : " << (4 ^ 8)<< endl;

    //bitwise Left Shift (<<) 
    cout << "bitwise Left Shift (<<) operator : " << (4 << 1)<< endl;

    //bitwise Right Shift (>>) 
    cout << "bitwise Right Shift (>>) operator : " << (10 >> 1)<< endl;


}